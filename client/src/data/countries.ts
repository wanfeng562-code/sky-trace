/** Build a fast IATA→country-code reverse index for dep/arr filter. */
export const AIRPORT_TO_COUNTRY: Record<string, string> = {};
for (const c of COUNTRIES) {
	for (const iata of c.airports) {
		AIRPORT_TO_COUNTRY[iata] = c.code;
	}
	if (c.regions) {
		for (const r of c.regions) {
			for (const iata of r.airports ?? []) {
				AIRPORT_TO_COUNTRY[iata] = c.code;
			}
		}
	}
}

