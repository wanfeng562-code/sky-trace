/**
 * Hierarchical geographic data for flight filtering (I-03).
 * Level 1: Country  (CountryInfo)
 * Level 2: Province / State / Region  (SubRegion)
 *
 * Bbox format: [latMin, latMax, lonMin, lonMax]
 * Note: city/district level would require a geocoding API and is out of scope
 * for static client-side data.
 */

export interface SubRegion {
	/** Short identifier, e.g. "BJ", "CA-BC", "JP-HKD" */
	code: string;
	nameZh: string;
	latMin: number;
	latMax: number;
	lonMin: number;
	lonMax: number;
	/** Key IATA codes for dep/arr filter in this region */
	airports?: string[];
}

export interface CountryInfo {
	code: string; // ISO 2-letter
	nameZh: string;
	latMin: number;
	latMax: number;
	lonMin: number;
	lonMax: number;
	airports: string[]; // representative IATA codes (country-level)
	regions?: SubRegion[]; // province / state / autonomous region
}

